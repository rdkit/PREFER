import random
import numpy as np


def set_random_seed(seed: int = 42):
    """
    Set random seed for random, numpy, tensorflow and pytorch
    Args:
        seed: the magic hyperparameter

    Returns: None

    """

    np.random.seed(seed)
    random.seed(seed)

    try:
        import tensorflow as tf

        tf_version = int(tf.version.VERSION.split(".")[0])

        if tf_version <= 1:
            tf.set_random_seed(seed)
        else:

            tf.random.set_seed(seed)
    except ModuleNotFoundError:
        print("Tensorflow not found; skipping: tf.random.set_seed(...)")
        pass
